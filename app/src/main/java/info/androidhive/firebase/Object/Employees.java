public class Employees {
    private String employeesName;
    private String employeesSkype;
    private String employeesEmail;
    private String employeesPhoneNumber;

    public String getEmployeesPhoneNumber() {
        return employeesPhoneNumber;
    }

    public void setEmployeesPhoneNumber(String employeesPhoneNumber) {
        this.employeesPhoneNumber = employeesPhoneNumber;
    }


    public String getEmployeesSkype() {
        return employeesSkype;
    }

    public void setEmployeesSkype(String employeesSkype) {
        this.employeesSkype = employeesSkype;
    }

    public String getEmployeesEmail() {
        return employeesEmail;
    }

    public void setEmployeesEmail(String employeesEmail) {
        this.employeesEmail = employeesEmail;
    }

    public String getEmployeesName() {
        return employeesName;
    }

    public void setEmployeesName(String employeesName) {
        this.employeesName = employeesName;
    }


}
